    private byte[] obtenerImagen(String productoRutaImagen) {
        File img = new File(productoRutaImagen);
        try{
            byte[] icono = new byte[ (int) img.length()  ]            ;
            InputStream inputStream = new FileInputStream(img);
            inputStream.read(icono);
            return icono;
        }catch(Exception e){
            System.out.println("error al convertir imagen" + e);
        return null;
        }
           
        
    }

