    private void btnClienteGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnClienteGuardarActionPerformed
        // TODO add your handling code here:
        
        if (!txtClienteEmail.getText().matches(EMAIL_PATTERN)) {
            JOptionPane.showMessageDialog(new JFrame(), "Email no válido", "Error",
                    JOptionPane.ERROR_MESSAGE);
            txtClienteEmail.requestFocus();
            return;
        }

        Cliente cliente = new Cliente();
        cliente.setId(Integer.parseInt(txtClienteId.getText()));
        cliente.setDocumento(txtClienteDocumento.getText());
        cliente.setNombre(txtClienteNombre.getText());
        cliente.setApellido(txtClienteApellido.getText());
        cliente.setDireccion(txtClienteDireccion.getText());
        cliente.setTelefono(txtClienteTelefono.getText());
        cliente.setCorreo(txtClienteEmail.getText());
        cliente.setEstado("A");

        if (cliente.getId() == 0) {
            if (ClienteDAO.insertarCliente(cliente)) {
                System.out.println("cliente=" + cliente.getId());
                txtClienteId.setText(cliente.getId().toString());
                JOptionPane.showMessageDialog(new JFrame(), "Registro guardado con éxito", "Success",
                        JOptionPane.INFORMATION_MESSAGE);
                bloquearCamposClientes(true);
                limpiarTabla(tablaCliente);
                listarClientes();

            } else {
                JOptionPane.showMessageDialog(new JFrame(), "Error al guardar registro", "Error",
                        JOptionPane.ERROR_MESSAGE);
            }
        } else {
            if (ClienteDAO.actualizarCliente(cliente)) {
                System.out.println("cliente=" + cliente.getId());
                txtClienteId.setText(cliente.getId().toString());
                JOptionPane.showMessageDialog(new JFrame(), "Registro actualizado con éxito", "Success",
                        JOptionPane.INFORMATION_MESSAGE);
                bloquearCamposClientes(true);
                limpiarTabla( tablaCliente ) ;
                listarClientes();

            } else {
                JOptionPane.showMessageDialog(new JFrame(), "Error al guardar registro", "Error",
                        JOptionPane.ERROR_MESSAGE);
            }
        }
        
        


        
    }//GEN-LAST:event_btnClienteGuardarActionPerformed

