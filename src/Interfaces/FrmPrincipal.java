    private void calcularSubTotal(JTable table){
        Double subTotal = 0.00;
        for (int i = 0; i < table.getModel().getRowCount(); i++) {
            subTotal = subTotal + Double.parseDouble( table.getValueAt(i, 6).toString());
        }
        String valor = new DecimalFormat("#0.00").format(subTotal );
        txtVentaProductoSubTotal.setText(valor);
    }
    
