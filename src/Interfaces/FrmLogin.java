    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        // TODO add your handling code here:
        if (txtLogin.getText().isEmpty()) {
            JOptionPane.showMessageDialog(new JFrame(), "Ingrese login", "Error",
                    JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (txtClave.getPassword().length == 0) {
            JOptionPane.showMessageDialog(new JFrame(), "Ingrese clave", "Error",
                    JOptionPane.ERROR_MESSAGE);
            return;
        }

        String login, clave;
        login = txtLogin.getText();
        clave = new String(txtClave.getPassword());
        
        Usuario u = UsuarioDAO.login(login, clave);

        if (null == u) {
            JOptionPane.showMessageDialog(new JFrame(), "Login o Clave inv√°lidos", "Error",
                    JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        JOptionPane.showMessageDialog(new JFrame(), "Bienvenido " + u.getNombreApellido() , "Success",
                    JOptionPane.INFORMATION_MESSAGE);
        
        FrmPrincipal frm = new FrmPrincipal();
        frm.setVisible(true);
        dispose();

        
        
        
    }//GEN-LAST:event_btnLoginActionPerformed

